package practice;

public class switchstatement {

	public static void main(String[] args) {

		int i=3;
		switch (i) {
		case 1:
			System.out.println("Sunday");
			break;
		case 2:
			System.out.println("Monday");
		default:
			System.out.println("Default Statement");
			break;
		}
	}

}
